public class SQ {
    private int id;
    private String qs;

    public SQ() {
    }

    public SQ(int id, String qs) {
        this.id = id;
        this.qs = qs;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getQs() {
        return qs;
    }

    public void setQs(String qs) {
        this.qs = qs;
    }

   
}
